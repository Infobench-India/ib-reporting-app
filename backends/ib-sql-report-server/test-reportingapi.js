async function hit(endpoint, ext) {
  const url = `${API}/${endpoint}`;
  const fileName = `${endpoint}-${Date.now()}.${ext}`;
  const outFile = path.resolve(__dirname, fileName);

  try {
    const res = await axios({
      method: 'POST',
      url,
      headers: { 'Content-Type': 'application/json' },
      data: body,                       // <-- now uses payload + data
      responseType: 'stream'
    });

    const writer = fs.createWriteStream(outFile);
    res.data.pipe(writer);

    await new Promise((resolve, reject) => {
      writer.on('finish', resolve);
      writer.on('error', reject);
    });

    console.log(`${endpoint.toUpperCase()}  -> ${res.status}  ${res.headers['content-type']}  ${fs.statSync(outFile).size} bytes saved to ${outFile}`);
  } catch (err) {
    console.log(err.message)
    // console.error(`${endpoint} failed:`, err.response ? err.response.data : err.message);
  }
}

